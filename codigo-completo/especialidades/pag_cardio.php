    <h1>Qual a área de atuação da Cardiologia?</h1>
<div class="container">
     <img src="./img/cardiologia 1.jpg" alt="cardio">
   <p>A Cardiologia é uma área da medicina que se dedica ao estudo, diagnóstico e tratamento das doenças do coração e dos vasos sanguíneos. Essa especialidade médica tem um papel de extrema importância na saúde das pessoas, pois o sistema cardiovascular desempenha um papel central na manutenção de todas as funções vitais do nosso organismo.

O coração é um órgão essencial para a vida, responsável por bombear o sangue rico em oxigênio para todas as partes do corpo. Ele é composto por diversas estruturas como válvulas, artérias, veias e músculos, que trabalham de forma coordenada para garantir o adequado fluxo sanguíneo. Portanto, qualquer disfunção nesse sistema pode ter consequências graves para a saúde.
A atuação do cardiologista envolve diversas etapas, desde a realização de exames clínicos até o desenvolvimento de planos de tratamento adequados para cada paciente. Ele tem a capacidade de interpretar e analisar dados como os resultados de eletrocardiogramas, ecocardiogramas, cateterismos cardíacos, entre outros exames específicos, para obter um diagnóstico preciso.</p></div>
