     <h1>Qual a importância da Geriatria?</h1>
     <div class="container">
     <img src="./img/geria2.jpg" alt="geriatria">
     <p>A importância dessa especialidade aumenta à medida que a expectativa de vida da população mundial aumenta. O envelhecimento populacional é uma realidade em muitos países, incluindo o Brasil, e é essencial oferecer cuidados de saúde adequados e voltados para essa parcela da população.
    
Dentre as diversas funções exercidas pela geriatria, destacam-se as avaliações globais do idoso, levando em consideração aspectos físicos, psicológicos e sociais. Isso possibilita um cuidado integral e personalizado, que respeita as particularidades de cada indivíduo.

Além disso, a geriatria atua também na prevenção de doenças e na promoção de hábitos saudáveis, como a prática de atividades físicas adequadas, alimentação balanceada e vacinação regular. Dessa forma, a especialidade busca prevenir o aparecimento de doenças e, quando necessário, intervir precocemente para evitar complicações e melhorar a qualidade de vida do idoso.

A presença do médico geriatra na equipe de saúde é fundamental para um envelhecimento saudável da população. A capacidade de diagnóstico e tratamento adequados, aliada à compreensão das necessidades específicas do idoso, contribui para a promoção de um maior bem-estar e autonomia nessa fase da vida.

É importante destacar que a geriatria não se trata apenas de cuidar das doenças, mas sim de oferecer um cuidado humanizado, proporcionando conforto, dignidade e respeito aos idosos. A medicina geriátrica é de vital importância para uma sociedade que valoriza o envelhecimento como uma etapa de vida a ser vivida com qualidade e bem-estar.
